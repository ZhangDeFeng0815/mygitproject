package com.tyyd.common.po;

import java.io.Serializable;

public class AssetIncomeTotal extends PoBaseInfo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1685440219594368574L;
	// ASSET_INCOME_D_ID 版权资产收入分类合计ID
	private Long id = null;
	// ASSET_ID:版权资产ID
	private Long assetId = null;
	// INCOME_ITEM_CD : 收入项CD
	private String incomeItemCd = null;
	// MONEY : 金额(分)
	private Long money = null;
	// CONTRACT_CD : 合同编号
	private String contractCd = null;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getAssetId() {
		return assetId;
	}

	public void setAssetId(Long assetId) {
		this.assetId = assetId;
	}

	public String getIncomeItemCd() {
		return incomeItemCd;
	}

	public void setIncomeItemCd(String incomeItemCd) {
		this.incomeItemCd = incomeItemCd;
	}

	public Long getMoney() {
		return money;
	}

	public void setMoney(Long money) {
		this.money = money;
	}

	public String getContractCd() {
		return contractCd;
	}

	public void setContractCd(String contractCd) {
		this.contractCd = contractCd;
	}

}
